class Solution:
    def isValidSudoku(self, board: list[list[str]]) -> bool:
        if not board: return False
        row, col, box = [], [] , []
        for _ in range (10):
            row.append([0] * 10)
            col.append([0] * 10)
            box.append([0] * 10)
        
        for i in range (9):    
            for j in range (9):
                val = board[i][j]
                if val == '.':
                    continue
                val = int(val)
                box_no = (i // 3) * 3 + j // 3

                if row[i][val] != 0 or \
                    col[j][val] != 0 or \
                    box[box_no][val] != 0:
                    return False
                
                row[i][val] = 1
                col[j][val] = 1
                box[box_no][val] = 1
        return True

